-- pengertian database
-- koleksi/kumpulan data informasi yang disimpan dalam server
-- bisa dalam bentuk apapun
-- data didatabase bisa diakses dari DBMS(database management system)
-- dbms ialah sebuah aplikasi untuk manage database

-- structured query language (SQL) ialah bahasa pemrograman untuk manage (CRUD) database dan data
-- fokus ke pengurangan data redundan
-- digambarkan dalam bentuk table
-- biasanya digunakan untuk penggunaan data besar

-- sql comands
-- 1. ddl - > membuat dan modifikasi db objek
-- 2. dml -> untuk manipulasi data
-- 3. dcl (data control language) -> membrikan hak, akses dan kontrol ke db system
-- 4. tcl -> mengatur transaksi db