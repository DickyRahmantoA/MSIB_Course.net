
public class AbstrackAndInterface {
	/*
	 * abstract class dibuat dengan cara menggunakan keyword abstract di depan class
	 * abstract class tidak dapat dibuat object, tetapi dapat diturunkan ke subclass
	 * dan buat object dari subclass
	 * 
	 * abstract method adalah method yang dideklarasi tanpa implementasi
	 * class yang memiliki abstract method harus didefinisikan sebagai abstract class,
	 * tidak ada code dalam function dan diakhiri dengan titik koma;
	 * 
	 * interface adalah class yang hanya berisi method constant dan abstract method
	 * inteface mirip abstract class, tetapi hanya berisi behavior umum dari object.
	 * deklarasi menggunakan keyword interface, dan subclass menggunakan keyword implements.
	 * interface tidak dapat dibuat object baru dengar keyword new
	 */
}
